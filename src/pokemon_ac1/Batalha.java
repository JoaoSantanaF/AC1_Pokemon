    private void battleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_battleActionPerformed
        int pinimigo = new Random().nextInt(125) +1;
        Pinimigo.setText(Integer.toOctalString(pinimigo));
        switch (play1){
            case 1:
                if(55 < pinimigo){
                    Perdedor.setVisible(true);
                }else if(55 > pinimigo){
                    Vencedor.setVisible(true);
                }else{
                    Empate.setVisible(true);
                }
                break;
                case 2:
                if(86 < pinimigo){
                    Perdedor.setVisible(true);
                }else if(86 > pinimigo){
                    Vencedor.setVisible(true);
                }else{
                    Empate.setVisible(true);
                }
                break;
                case 3:
                if(120 < pinimigo){
                    Perdedor.setVisible(true);
                }else if(120 > pinimigo){
                    Vencedor.setVisible(true);
                }else{
                    Empate.setVisible(true);
                }
                break;
                case 4:
                if(98 < pinimigo){
                    Perdedor.setVisible(true);
                }else if(98 > pinimigo){
                    Vencedor.setVisible(true);
                }else{
                    Empate.setVisible(true);
                }
                break;
        }
        
    }//GEN-LAST:event_battleActionPerformed

